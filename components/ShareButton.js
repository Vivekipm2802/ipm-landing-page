function generateShareLink(platformName, contentToShare) {
    platformName = platformName.toLowerCase();
    switch (platformName) {
        case "instagram":
            return `https://www.instagram.com/share?url=${encodeURIComponent(contentToShare)}`;
        case "facebook":
            return `https://www.facebook.com/sharer/sharer.php?u=${encodeURIComponent(contentToShare)}`;
        case "whatsapp":
            return `https://wa.me/?text=${encodeURIComponent(contentToShare)}`;
        case "email":
            return `mailto:?body=${encodeURIComponent(contentToShare)}`;
        case "linkedin":
            return `https://www.linkedin.com/shareArticle?url=${encodeURIComponent(contentToShare)}`;
        case "pinterest":
            return `https://www.pinterest.com/pin/create/button/?url=${encodeURIComponent(contentToShare)}`;
        default:
            return `Unsupported social platform: ${platformName}`;
    }
}




